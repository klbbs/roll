#include <bits/stdc++.h>
using namespace std;
void Solve() {
	int n;
	cin >> n;
	if (n <= 3 || n == 5) {
		puts("-1");
		return ;
	}
	if (n % 4 == 0) {
		for (int i = 0; i < n / 4; i++) {
			puts("0 0 0\n1 0 0\n1 1 0\n0 1 0");
		}
		puts(n == 4 ? "1" : "inf");
		return ;
	}
	if (n % 4 == 3) {
		for (int i = 1; i < n / 4; i++) {
			puts("0 0 0\n1 0 0\n1 1 0\n0 1 0");
		}
		puts("0.000000 0.000000 0.000000");
		puts("1.000000 0.000000 0.000000");
		puts("1.000000 0.000000 1.000000");
		puts("0.000000 0.000000 1.000000");
		puts("0.000000 0.978318 1.207107");
		puts("0.691080 1.128011 0.500000");
		puts("0.000000 0.978318 -0.207107");
		puts("inf");
		return ;
	}
	if (n % 4 == 2) {
		for (int i = 1; i < n / 4; i++) {
			puts("0 0 0\n1 0 0\n1 1 0\n0 1 0");
		}
		puts("0 0 0\n1 0 0\n1 0 1\n1 1 1\n0 1 1\n0 1 0");
		puts("inf");
		return ;
	}
	if (n % 4 == 1) {
		for (int i = 2; i < n / 4; i++) {
			puts("0 0 0\n1 0 0\n1 1 0\n0 1 0");
		}
		puts("-0.000000 -0.000000 -0.000000");
		puts("1.000000 0.000000 0.000000");
		puts("1.000000 0.000000 1.000000");
		puts("0.000000 0.000000 1.000000");
		puts("0.000000 -0.471126 0.117934");
		puts("0.313874 0.366365 -0.329383");
		puts("-0.631572 0.685350 -0.395566");
		puts("-0.494402 1.259397 0.411685");
		puts("-0.000000 0.513536 0.858068");
		puts("inf");
		return ;
	}
	assert(0);
}
signed main() {
	int t;
	scanf("%d", &t);
	while (t--) Solve();
	return 0;
}